package me.berkesongur.hesapmakinesi.islemler

class Islem {

    fun Sum(a: Float, b: Float): Float {
        return a + b
    }
    fun Sub(a: Float, b: Float): Float {
        return a - b
    }
    fun Mult(a: Float, b: Float): Float {
        return a * b
    }
    fun Divide(a: Float, b: Float): Float {
        return a / b
    }

}